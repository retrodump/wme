#include "Wme.h"
#include "File.h"


namespace Wme
{

	
//////////////////////////////////////////////////////////////////////////
File::File()
{
	m_Size = 0;
	m_Pos = 0;
}

//////////////////////////////////////////////////////////////////////////
File::~File()
{
}

//////////////////////////////////////////////////////////////////////////
size_t File::GetSize()
{
	return m_Size;
}

//////////////////////////////////////////////////////////////////////////
size_t File::GetPos()
{
	return m_Pos;
}

//////////////////////////////////////////////////////////////////////////
bool File::IsEOF()
{
	return m_Pos >= m_Size;
}

//////////////////////////////////////////////////////////////////////////
WideString File::GetFileName()
{
	return m_FileName;
}


} // namespace Wme
